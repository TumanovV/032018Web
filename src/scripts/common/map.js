function initMap(){
  var element = document.getElementById('map');
  var options = {
    zoom: 10,
    center: {lat: 54.609543, lng: 39.712585}
  };

  var myMap = new google.maps.Map(element, options);

  var marker = new google.maps.Marker({
    position: {lat: 54.63463, lng: 39.692274},
    map: myMap
  });
}